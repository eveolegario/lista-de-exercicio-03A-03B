package lista03b.exercicio1;

public class Numeral {

    public static void main(String[] args) {

        for (int i = 1; i <= 100; i++) {
            System.out.println(i);
        }
        int contador = 1;
        while (contador <= 100) {
            System.out.println(contador);
            contador++;
        }

    }
}
